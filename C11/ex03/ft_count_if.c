int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int c;
	int	i;

	i = 0;
	c = 0;
	while (tab[i])
	{
		if (f(tab[i]) != 0)
			c++;
		i++;
	}
	return (c);
}
